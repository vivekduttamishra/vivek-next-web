class BookService {
    constructor() {
        this.books = books
    }

    async addBook(book) {
        this.books.push(book);
    }

    async getBooks(match) {
        if (match) {
            return this.books.filter(match);
        }
        return this.books;
    }

    async getBooksByTag(tag) {
        return this.books.filter(book => book.tags.includes(tag));
    }

    async getBooksBySeries(series) {
        return this
            .books
            .filter(book => book.series && book.series.name === series)
            .sort((a, b) => a.series.number - b.series.number);
    }

    async getBookById(id) {
        return this.books.find(book => book.id === id);
    }
}

export default new BookService();